package tasks.month1.sprint1;

/**
 * QuotedDialogues - Displays a conversation between Teacher and Student.
 * Demonstrates the use of escape character \" for embedded quotes.
 */
public class QuotedDialogues {
    public static void main(String[] args) {
        // Teacher's first question (quotes escaped with \")
        System.out.println("Teacher: \"What is your name?\"");
        // Student's response (quotes escaped with \")
        System.out.println("Student: \"My name is Alex.\"");
        // Teacher's welcoming message (quotes escaped with \")
        System.out.println("Teacher: \"Welcome, Alex!\"");
    }
}