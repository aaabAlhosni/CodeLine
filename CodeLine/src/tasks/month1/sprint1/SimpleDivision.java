package tasks.month1.sprint1;

/**
 * SimpleDivision - Demonstrates basic integer division in Java.
 * Divides one integer by another and prints the quotient (result).
 */
public class SimpleDivision {
    public static void main(String[] args) {
        // Total value to be divided (dividend)
        int total = 100;
        // Number of equal parts (divisor)
        int parts = 5;
        // Calculate quotient using division operator
        int quotient = total / parts;
        // Print the result (outputs: 20)
        System.out.println(quotient);
    }
}