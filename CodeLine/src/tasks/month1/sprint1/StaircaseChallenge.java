package tasks.month1.sprint1;

/**
 * StaircaseChallenge - Prints a staircase pattern using hash symbols.
 * Demonstrates basic output printing without loops or conditionals.
 */
public class StaircaseChallenge {
    public static void main(String[] args) {
        // First step: single hash
        System.out.println("#");
        // Second step: two hashes
        System.out.println("##");
        // Third step: three hashes
        System.out.println("###");
        // Fourth step: four hashes
        System.out.println("####");
        // Fifth step: five hashes (top of staircase)
        System.out.println("#####");
    }
}