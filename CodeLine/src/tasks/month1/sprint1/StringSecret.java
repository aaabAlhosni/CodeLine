package tasks.month1.sprint1;

/**
 * StringSecret - Demonstrates using the length() method on a String.
 * Prints the number of characters in the given string using built-in method.
 */
public class StringSecret {
    public static void main(String[] args) {
        // Declare and initialize a String variable with value "Java Programming"
        String text = "Java Programming";
        // Print the length of the string using the built-in length() method
        System.out.println("Length: " + text.length());
    }
}