package tasks.month1.sprint1;

/**
 * CaseChanger - Demonstrates converting strings to uppercase and lowercase.
 * Uses built-in String methods toChange case of the text.
 */
public class CaseChanger {
    public static void main(String[] args) {
        // Declare a String variable with mixed case text
        String text = "Sprint One";
        // Convert to uppercase and print: "SPRINT ONE"
        System.out.println(text.toUpperCase());
        // Convert to lowercase and print: "sprint one"
        System.out.println(text.toLowerCase());
    }
}