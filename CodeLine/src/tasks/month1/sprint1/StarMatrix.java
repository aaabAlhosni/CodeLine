package tasks.month1.sprint1;

/**
 * StarMatrix - Prints a 5x5 grid of asterisks with single space between each.
 * Demonstrates basic output without loops, arrays, or conditionals.
 */
public class StarMatrix {
    public static void main(String[] args) {
        // Row 1: Five asterisks separated by spaces
        System.out.println("* * * * *");
        // Row 2: Same pattern
        System.out.println("* * * * *");
        // Row 3: Same pattern
        System.out.println("* * * * *");
        // Row 4: Same pattern
        System.out.println("* * * * *");
        // Row 5: Same pattern (completes 5x5 grid)
        System.out.println("* * * * *");
    }
}