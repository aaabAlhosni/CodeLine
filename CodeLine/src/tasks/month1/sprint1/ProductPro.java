package tasks.month1.sprint1;

/**
 * ProductPro - Demonstrates basic multiplication with double values.
 * Multiplies two decimal numbers and prints the result.
 */
public class ProductPro {
    public static void main(String[] args) {
        // First double operand (decimal number)
        double num1 = 12.5;
        // Second double operand (decimal number)
        double num2 = 4.0;
        // Calculate product using multiplication operator
        double product = num1 * num2;
        // Print the result (outputs: 50.0)
        System.out.println(product);
    }
}