/**
 * QuotedMessage - Prints a phrase with embedded quotation marks.
 * Uses escape character \" to display quotes in the output without ending the string.
 */
public class QuotedMessage {
    public static void main(String[] args) {
        // Prints the phrase with inner quotes using escape sequence \"
        System.out.println("He said, \"Practice makes perfect!\"");
    }
}